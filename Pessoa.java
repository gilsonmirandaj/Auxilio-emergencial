package classes;

public class Pessoa {
	
	private String nome;
	private int idade, numDePessoas;
	private char sexo, recebeBolsa;

	
	public char getRecebeBolsa() {
		return recebeBolsa;
	}
	public void setRecebeBolsa(char recebeBolsa) {
		this.recebeBolsa = recebeBolsa;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public int getNumDePessoas() {
		return numDePessoas;
	}
	public void setNumDePessoas(int numDePessoas) {
		this.numDePessoas = numDePessoas;
	}
	public char getSexo() {
		return sexo;
	}
	public void setSexo(char sexo) {
		this.sexo = sexo;
	}
	

}
